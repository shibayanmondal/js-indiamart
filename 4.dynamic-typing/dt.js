// premitive types
var a = 10 //typeof a = number(integer/floating)
    a = "scott" //typeof a = string
    a = true //typeof a = boolean
    a = null //typeof a = object (we can set this to a variable)
    a = undefined //typeof a = undefined (we should never use this)

// Object types 
    a = {} //typeof a = object
    a = function(){} //typeof a = function object
    a = [] //typeof a = object
